package model;

/**
 *
 * @author viet2
 */
public class OrderDetail {
    private int id;
    private int pid;
    private int quantity;
    private double price;

    public OrderDetail() {
    }

    public OrderDetail(int id, int pid, int quantity, double price) {
        this.id = id;
        this.pid = pid;
        this.quantity = quantity;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
    
    
}
